print("find the Factorial of an Number !")

print("")

num = int(input("Please enter a Number : "))  # Hare we created a variable NUM to take the input from User.

result = 1                                    # This our another variable which can used to store the result of our factorial

for i in range(1,num+1):                      # Here we used a for-loop to interate the variable i in between 1 to  given number.
    result = result * i                       # Here we are actually used our variable result to store our factorial
    
print("")
print(f"The factorial of {num} is {result}")  # Here we used f-string to print the values of variable in our print statement
